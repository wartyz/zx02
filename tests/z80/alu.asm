        DEVICE ZXSPECTRUM48
        ORG 0000h

START:
; -----------------------------------------------------
; Inicializaci√≥n
; -----------------------------------------------------
        LD A,10h
        LD B,01h
        LD C,02h
        LD D,03h
        LD E,04h
        LD H,05h
        LD L,06h

; -----------------------------------------------------
; ADD A,r
; -----------------------------------------------------
        ADD A,B
        ADD A,C
        ADD A,D
        ADD A,E
        ADD A,H
        ADD A,L
        ADD A,A

; -----------------------------------------------------
; ADC A,r
; -----------------------------------------------------
        ADC A,B
        ADC A,C
        ADC A,D
        ADC A,E
        ADC A,H
        ADC A,L
        ADC A,A

; -----------------------------------------------------
; SUB r
; -----------------------------------------------------
        SUB B
        SUB C
        SUB D
        SUB E
        SUB H
        SUB L
        SUB A

; -----------------------------------------------------
; SBC A,r
; -----------------------------------------------------
        SBC A,B
        SBC A,C
        SBC A,D
        SBC A,E
        SBC A,H
        SBC A,L
        SBC A,A

; -----------------------------------------------------
; AND r
; -----------------------------------------------------
        AND B
        AND C
        AND D
        AND E
        AND H
        AND L
        AND A

; -----------------------------------------------------
; OR r
; -----------------------------------------------------
        OR B
        OR C
        OR D
        OR E
        OR H
        OR L
        OR A

; -----------------------------------------------------
; XOR r
; -----------------------------------------------------
        XOR B
        XOR C
        XOR D
        XOR E
        XOR H
        XOR L
        XOR A

; -----------------------------------------------------
; CP r
; -----------------------------------------------------
        CP B
        CP C
        CP D
        CP E
        CP H
        CP L
        CP A

; -----------------------------------------------------
; Inmediatos
; -----------------------------------------------------
        ADD A,55h
        ADC A,11h
        SUB 22h
        SBC A,33h
        AND 0Fh
        OR 80h
        XOR FFh
        CP 10h

; -----------------------------------------------------
; Fin
; -----------------------------------------------------
        HALT

; -----------------------------------------------------
; Salida binaria
; -----------------------------------------------------
        SAVEBIN "alu.bin", 0, $
